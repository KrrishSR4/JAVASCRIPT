// dom navigation properties are helpful when the elements are close to each other. If they are not clpse to each other, we have some methods to search the dom. 


document.getElementById //this method is used to get the element with a given "id" attribute

document.querySelectorAll //returns all elements inside an element matching the given css selector

document.querySelector //returns the first element inside an element matching the given css selector

document.getElementsByClassName //returns all elements inside an element with the given class name  

document.getElementsByTagName //returns all elements inside an element with the given tag name