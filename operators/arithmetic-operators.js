// +, -, x, /, %

//example-1

let a = 45;
let b = 4
//addition
console.log("a + b =", a + b)//output:- a + b = 49
//subtraction
console.log("a - b =", a - b)//output:- a - b = 41
//multiplication
console.log("a * b =", a * b)//output:- a * b = 180
//division
console.log("a / b =", a / b)//output:- a / b = 11.25
//modulus
console.log("a % b =", a % b)//output:- a % b = 1
//exponentiation
console.log("a ** b =", a ** b)//output:- a ** b = 4100625


//example -2(increment and decrement operators)
let a1 = 10;

console.log("a1++ =", a1++)//output:- a1++ = 10
console.log("a1=", a1)//output:- a1= 11
console.log("++a1 =", ++a1)//output:- ++a1 = 12

let b1 = 10;

console.log("b1-- =", b1--)//output:- b1-- = 10
console.log("b1=", b1)//output:- b1= 9
console.log("--b1 =", --b1)//output:- --b1 = 8
