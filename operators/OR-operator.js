// or operator (||)

let x = 5;
let y = 6; 
console.log(x < y  || x!==5) // true because one condition is true
console.log(x > y  || x!==5) // true because one condition is true
console.log(x > y  || x===5) // false because both conditions are false
console.log(x < y  || x===5) // true because both conditions are true


